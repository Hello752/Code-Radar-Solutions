#include <stdio.h>

int main() {
    int num ; // Example number

    printf("Decimal value is: %d\n", num);
    printf("Octal value is: %o\n", num);
    printf("Hexadecimal value is (Alphabet in small letters): %x\n", num);
    printf("Hexadecimal value is (Alphabet in capital letters): %X\n", num);

    return 0;
}